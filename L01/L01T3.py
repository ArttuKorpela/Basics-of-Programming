Luku1 = 123
Luku2 = 456

Summa = Luku1+Luku2
Erotus = Luku2-Luku1
Tulos = Summa*Erotus

print(f"{Luku1} + {Luku2} = {Summa}")
print(f"{Luku2} - {Luku1} = {Erotus}")
print(f"{Summa} * {Erotus} = {Tulos}")
print(f"( {Luku1} + {Luku2} ) * ( {Luku2} - {Luku1} ) = {Tulos}")

